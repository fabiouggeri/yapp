public class ParserGenerationException extends Exception {

   public ParserGenerationException() {
   }

   public ParserGenerationException(String message) {
      super(message);
   }

   public ParserGenerationException(Throwable cause) {
      super(cause);
   }

   public ParserGenerationException(String message, Throwable cause) {
      super(message, cause);
   }
   
}
